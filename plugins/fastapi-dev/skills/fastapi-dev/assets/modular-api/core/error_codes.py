"""错误码枚举"""

from enum import IntEnum


class ErrorCode(IntEnum):
    """业务错误码

    分段规则:
    - 0: 成功
    - 10000-19999: 系统级错误
    - 20000-29999: 服务级错误
    - 30000-39999: 业务校验错误
    - 40000-49999: 客户端请求错误
    - 50000-59999: 外部依赖错误
    """

    SUCCESS = 0

    # 10000-19999: 系统级错误
    SYSTEM_ERROR = 10000
    DATABASE_ERROR = 10001
    CACHE_ERROR = 10002

    # 20000-29999: 服务级错误
    SERVICE_UNAVAILABLE = 20000
    SERVICE_TIMEOUT = 20001

    # 30000-39999: 业务校验错误
    RESOURCE_NOT_FOUND = 30000
    USER_NOT_FOUND = 30001
    DUPLICATE_ENTRY = 30100
    USERNAME_ALREADY_EXISTS = 30101
    USER_NOT_DELETED = 30102

    # 40000-49999: 客户端请求错误
    INVALID_REQUEST = 40000
    INVALID_PARAMETER = 40001
    MISSING_PARAMETER = 40002
    UNAUTHORIZED = 40100
    TOKEN_EXPIRED = 40101
    TOKEN_INVALID = 40102
    FORBIDDEN = 40200
    USER_DISABLED = 40201
    INSUFFICIENT_PERMISSIONS = 40202

    # 50000-59999: 外部依赖错误
    EXTERNAL_API_ERROR = 50000
    EXTERNAL_API_TIMEOUT = 50001
